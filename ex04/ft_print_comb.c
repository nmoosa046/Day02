void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a= '0';
	while(a <= '7')
	{
		b = a + 1;
		while ( b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				if (a != '0' || b != '1' || c !='2')
					ft_putchar(',');
				if (a != '0' || b != '1' || c !='2')
					ft_putchar(' ');
				ft_putchar(a);
				ft_putchar(b);
				ft_putchar(c);
				c = c + 1;
			}
			b = b + 1;
		}
		a = a + 1;
	}
}

